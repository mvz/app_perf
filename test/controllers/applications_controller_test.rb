# frozen_string_literal: true

require 'test_helper'

class ApplicationsControllerTest < ActionController::TestCase
  setup do
    @application = applications(:one)
  end

  test 'should get index' do
    get :index
    assert_response :success
    assert_not_nil assigns(:applications)
  end

  test 'should get new' do
    get :new
    assert_response :success
  end

  test 'should create application' do
    assert_difference('Application.count') do
      post :create, params: { application: { license_key: @application.license_key, name: @application.name, user_id: @application.user_id } }
    end

    assert_redirected_to application_path(assigns(:application))
  end

  test 'should show application' do
    get :show, params: { id: @application }
    assert_response :success
  end

  test 'should get edit' do
    get :edit, params: { id: @application }
    assert_response :success
  end

  test 'should update application' do
    patch :update, params: { id: @application, application: { license_key: @application.license_key, name: @application.name, user_id: @application.user_id } }
    assert_redirected_to application_path(assigns(:application))
  end

  test 'should destroy application' do
    assert_difference('Application.count', -1) do
      delete :destroy, params: { id: @application }
    end

    assert_redirected_to applications_path
  end
end
